// HOW TO READ A OBJECT

let employees = { id:101 , name:"9SAI" , salary:45000 }
console.log(employees)                                  // { id: 101, name: '9SAI', salary: 45000 }
console.log(employees.id)                               // 101
console.log(employees.name)                             // 9SAI
console.log(employees.salary)                           // 45000