// HOW TO ADD AND UPDATE A PROPERTY IN A OBJECT

let employee = { id:101 , name:"9SAI" , salary:45000 }
employee.location = "KADAPA"
console.log(employee)                                 // { id: 101, name: '9SAI', salary: 45000, location: 'KADAPA' }
console.log(employee.location)                        // KADAPA