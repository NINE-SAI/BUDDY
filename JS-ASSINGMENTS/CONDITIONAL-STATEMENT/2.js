// WRITE A PROGRAM TO CHECK IF A NUMBER IS MULTIPLE OF 3 OR NOT?

var a = 90 ;
if (a % 3 == 0) {
    console.log("MULTIPLE OF 3")
}
else {
    console.log("NOT A MULTIPLE OF 3")
}                                                       //MULTIPLE OF 3

// FOR FALSE STATEMENT
var a = 16 ;
if (a % 3 == 0) {
    console.log("MULTIPLE OF 3")
}
else {
    console.log("NOT A MULTIPLE OF 3")
}                                                       //NOT A MULTIPLE OF 3