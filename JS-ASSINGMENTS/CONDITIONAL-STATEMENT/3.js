// WRITE  A PROGRAM TO CHECK IF A NUMBER IS POSITIVE OR NOT

var a = 25 ;
if (a >= 0) {
    console.log("NUMBER IS POSITIVE")
}
else {
    console.log("NUMBER IS NOT POSITIVE")
}                                                       //POSITIVE NUMBER

// FOR FALSE STATEMENT
var a = -14 ;
if (a >= 0) {
    console.log("NUMBER IS POSITIVE")
}
else {
    console.log("NUMBER IS NOT POSITIVE")
}                                                       //NOT A POSITIVE NUMBER